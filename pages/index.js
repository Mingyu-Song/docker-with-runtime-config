import Head from "next/head";
import styles from "../styles/Home.module.css";

export function getEnv(envName) {
  if (!envName) {
    return undefined;
  }

  if (typeof window !== "undefined") {
    const { __ENV } = window;
    return __ENV[envName];
  } else {
    return process.env[envName];
  }
}

